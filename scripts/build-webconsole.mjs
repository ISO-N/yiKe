import { mkdir, readFile, readdir, stat, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const webConsoleRoot = path.join(repoRoot, "app", "src", "main", "assets", "webconsole");
const entryRoot = path.join(webConsoleRoot, "entries");
const checkOnly = process.argv.includes("--check");

const entryTargets = [
    {
        input: path.join(entryRoot, "app.entry.js"),
        output: path.join(webConsoleRoot, "app.js"),
        banner: "// Generated by scripts/build-webconsole.mjs. Do not edit directly.\n",
    },
    {
        input: path.join(entryRoot, "app.entry.css"),
        output: path.join(webConsoleRoot, "app.css"),
        banner: "/* Generated by scripts/build-webconsole.mjs. Do not edit directly. */\n",
    },
];

const sizeBudgets = [
    {
        directory: path.join(webConsoleRoot, "scripts", "shell"),
        maxBytes: 24000,
        label: "shell 模块",
    },
    {
        directory: path.join(webConsoleRoot, "scripts", "shared"),
        maxBytes: 14000,
        label: "shared 模块",
    },
    {
        directory: path.join(webConsoleRoot, "scripts", "features"),
        maxBytes: 22000,
        label: "feature 模块",
    },
    {
        directory: path.join(webConsoleRoot, "styles"),
        maxBytes: 18000,
        label: "样式层",
    },
];

/**
 * 主流程集中调度，是为了让网页后台的资源生成与体量守护共享同一条可复用的命令入口。
 */
async function main() {
    await mkdir(entryRoot, { recursive: true });
    const changedOutputs = [];
    for (const target of entryTargets) {
        const changed = await syncEntryTarget(target);
        if (changed) {
            changedOutputs.push(path.relative(repoRoot, target.output));
        }
    }
    await verifySizeBudgets();
    if (checkOnly) {
        if (changedOutputs.length) {
            throw new Error(`网页后台入口产物未同步：\n- ${changedOutputs.join("\n- ")}`);
        }
        console.log("WebConsole assets are up to date.");
        return;
    }
    if (changedOutputs.length) {
        console.log(`Updated WebConsole assets:\n- ${changedOutputs.join("\n- ")}`);
    } else {
        console.log("WebConsole assets already up to date.");
    }
}

/**
 * 单个入口产物同步在单点完成，是为了让 JS 与 CSS 入口都遵循同样的生成边界和换行约束。
 */
async function syncEntryTarget(target) {
    const source = `${target.banner}${(await readFile(target.input, "utf8")).trimEnd()}\n`;
    const previous = await readTextIfExists(target.output);
    if (previous === source) {
        return false;
    }
    if (checkOnly) {
        return true;
    }
    await writeFile(target.output, source, "utf8");
    return true;
}

/**
 * 文件存在时读取文本、不存在时返回空值，是为了让生成脚本既能首次产出也能做增量校验。
 */
async function readTextIfExists(targetPath) {
    try {
        return await readFile(targetPath, "utf8");
    } catch (error) {
        if (error.code === "ENOENT") {
            return null;
        }
        throw error;
    }
}

/**
 * 体量门禁按目录执行，是为了尽早阻止网页后台再次退化为超大脚本或超大样式文件。
 */
async function verifySizeBudgets() {
    for (const budget of sizeBudgets) {
        const fileNames = await readdir(budget.directory);
        for (const fileName of fileNames.filter((item) => item.endsWith(".js") || item.endsWith(".css"))) {
            const absolutePath = path.join(budget.directory, fileName);
            const fileStat = await stat(absolutePath);
            if (fileStat.size > budget.maxBytes) {
                throw new Error(
                    `${budget.label}超出体量预算：${path.relative(repoRoot, absolutePath)} 为 ${fileStat.size} bytes，限制为 ${budget.maxBytes} bytes。`
                );
            }
        }
    }
}

main().catch((error) => {
    console.error(error.message);
    process.exitCode = 1;
});
